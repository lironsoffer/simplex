"""
Dataset generation for the non-ergodic Mess3 transformer experiment.

Each training sequence is generated entirely by one sampled Mess3 component.
Ground-truth belief states are computed analytically at every token position.

Returns:
    tokens: (N, L) int64 tensor
    pi_targets: (N, L, K) float32 — meta-belief at each position (after update)
    eta_targets: (N, L, K, 3) float32 — within-component beliefs at each position
    component_ids: (N,) int64 — which component generated each sequence
"""

import numpy as np
import torch
from torch.utils.data import Dataset, DataLoader

from src.data.mess3 import Mess3HMM, build_default_components
from src.data.belief_update import (
    compute_belief_trajectory,
    beliefs_to_arrays,
)


class Mess3Dataset(Dataset):
    """
    PyTorch Dataset of Mess3 sequences with ground-truth belief state targets.

    Each item is a single sequence of length L generated by one component.
    Belief states are indexed from t=1 (after observing the first token) so
    they align with the transformer's residual stream at each position.
    """

    def __init__(
        self,
        tokens: torch.Tensor,
        pi_targets: torch.Tensor,
        eta_targets: torch.Tensor,
        component_ids: torch.Tensor,
    ):
        """
        Args:
            tokens: (N, L) int64
            pi_targets: (N, L, K) float32, belief AFTER observing each token
            eta_targets: (N, L, K, 3) float32, belief AFTER observing each token
            component_ids: (N,) int64
        """
        self.tokens = tokens
        self.pi_targets = pi_targets
        self.eta_targets = eta_targets
        self.component_ids = component_ids

    def __len__(self) -> int:
        return self.tokens.shape[0]

    def __getitem__(self, idx: int) -> dict:
        return {
            "tokens": self.tokens[idx],               # (L,)
            "pi_targets": self.pi_targets[idx],       # (L, K)
            "eta_targets": self.eta_targets[idx],     # (L, K, 3)
            "component_id": self.component_ids[idx],  # scalar
        }


def generate_sequences(
    n_sequences: int,
    seq_length: int,
    components: list[Mess3HMM] | None = None,
    seed: int = 42,
) -> tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    """
    Generate sequences and ground-truth belief states.

    Args:
        n_sequences: number of sequences to generate
        seq_length: length of each sequence (context window)
        components: list of Mess3HMM instances (default: CLAUDE.md parameters)
        seed: random seed for reproducibility

    Returns:
        tokens: (N, L) int64
        pi_targets: (N, L, K) float32 — belief AFTER each token
        eta_targets: (N, L, K, 3) float32 — belief AFTER each token
        component_ids: (N,) int64
    """
    if components is None:
        components = build_default_components()

    K = len(components)
    rng = np.random.default_rng(seed)

    tokens_all = np.zeros((n_sequences, seq_length), dtype=np.int64)
    pi_all = np.zeros((n_sequences, seq_length, K), dtype=np.float32)
    eta_all = np.zeros((n_sequences, seq_length, K, 3), dtype=np.float32)
    comp_ids = np.zeros(n_sequences, dtype=np.int64)

    for i in range(n_sequences):
        # Sample which component generates this sequence
        k = rng.integers(0, K)
        comp_ids[i] = k
        comp = components[k]

        # Generate token sequence from component k
        tokens, _ = comp.generate_sequence(seq_length, rng=rng)
        tokens_all[i] = tokens

        # Compute belief trajectory (K components update simultaneously)
        beliefs = compute_belief_trajectory(tokens, components)

        # beliefs[0] = initial (t=0, before any tokens)
        # beliefs[t+1] = after observing tokens[0..t]
        # We want belief AFTER token t for position t → beliefs[1..L]
        pi_traj, eta_traj = beliefs_to_arrays(beliefs)

        # pi_traj: (L+1, K), take indices 1..L+1
        pi_all[i] = pi_traj[1:]     # (L, K)
        eta_all[i] = eta_traj[1:]   # (L, K, 3)

    return tokens_all, pi_all, eta_all, comp_ids



def collect_activations_and_targets(
    dataset: Mess3Dataset,
    max_sequences: int | None = None,
) -> dict:
    """
    Collect all tokens, belief targets, and component IDs from a dataset into
    a single dict of numpy arrays for analysis.

    Args:
        dataset: Mess3Dataset
        max_sequences: optional limit on number of sequences

    Returns:
        dict with keys: tokens, pi_targets, eta_targets, component_ids
    """
    N = len(dataset) if max_sequences is None else min(len(dataset), max_sequences)
    return {
        "tokens": dataset.tokens[:N].numpy(),
        "pi_targets": dataset.pi_targets[:N].numpy(),
        "eta_targets": dataset.eta_targets[:N].numpy(),
        "component_ids": dataset.component_ids[:N].numpy(),
    }
